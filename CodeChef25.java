package com.Rishabh;

import java.util.Scanner;

public class CodeChef25 {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        int t = in.nextInt();
        while(t-->0){
            int n = in.nextInt();
            for(int i=0;i<n;i++){
                for(int j=0;j<n;j++){
                    System.out.print(1);
                }
                System.out.println();
            }
        }
    }
}
